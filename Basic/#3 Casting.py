# INTEGER
print("\nINTEGER\n")
data_int = 10
print("Input = ", data_int, type(data_int))

int_to_float = float(data_int)
int_to_str = str(data_int)
int_to_bool = bool(data_int)

print("Input = ", int_to_float, type(int_to_float))
print("Input = ", int_to_str, type(int_to_str))
print("Input = ", int_to_bool, type(int_to_bool))

#STRING
print("\nSTRING\n")
data_str = "3125"
print("Input = ", data_str, type(data_str))

str_to_float = float(data_str)
str_to_int = int(data_str)
str_to_bool = bool(data_str)

print("Input = ", str_to_float, type(str_to_float))
print("Input = ", str_to_int, type(str_to_int))
print("Input = ", str_to_bool, type(str_to_bool))

